$(document).ready(function(){
  $('.hidden-form').hide();
  $('.hidden-button').hide();
  $(".find-a-party").click(function(){
    $('.hidden-form').slideToggle(function(){
      $(this).children(".hidden-drop").focus()
    });
  });
  $(".plan-a-party").click(function(){
    $('.hidden-form').slideToggle(function(){
      $(this).children(".hidden-drop").focus()
    });
  });
});
